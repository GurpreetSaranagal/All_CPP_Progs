#include<iostream>
using namespace std;

int main()
{
    int num;
    cout<<"Enter any value: ";
    cin>>num;

    int fact=1;
    for(int i=1; i<=num; i++){
        fact=fact*i;
    }
    cout<<"factorial = "<<fact;
    return 0;
}
